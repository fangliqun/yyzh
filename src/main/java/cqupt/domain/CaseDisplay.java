package cqupt.domain;

public class CaseDisplay {
    private Integer caseId;

    private String url;

    private int caseDisplayId;

	public Integer getCaseId() {
		return caseId;
	}

	public void setCaseId(Integer caseId) {
		this.caseId = caseId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getCaseDisplayId() {
		return caseDisplayId;
	}

	public void setCaseDisplayId(int caseDisplayId) {
		this.caseDisplayId = caseDisplayId;
	}
    
}