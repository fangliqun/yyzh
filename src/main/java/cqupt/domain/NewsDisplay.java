package cqupt.domain;

public class NewsDisplay {
    private String url;
    private int id ;
    private int newsdisplayid;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNewsdisplayid() {
		return newsdisplayid;
	}
	public void setNewsdisplayid(int newsdisplayid) {
		this.newsdisplayid = newsdisplayid;
	}

  
}